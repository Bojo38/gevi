    private void jbtOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtOKActionPerformed
       Executant newUser=new Executant();
       newUser.setAddress(jtaAdresse.getText());
       newUser.setCourriel(jtfCourriel.getText());
       newUser.setDivers(jtaDivers.getText());
       newUser.setNom(jtfNom.getText());
       newUser.setPrenom(jtfPrenom.getText());
       newUser.setTelephone(jtfTelephone.getText());

       if (newUser.getNom().equals(""))
       {
           JOptionPane.showMessageDialog(this, "Le champ NOM est vide","Champ manquant",JOptionPane.ERROR_MESSAGE);
       }
       else
       {
          boolean alreadyExist=false;
          for (int i=0; i<_data.size(); i++)
          {
              Executant user=(Executant)_data.get(i);
              if (user.getNom().equals(newUser.getNom()))
              {
                  if (user.getPrenom().equals(newUser.getPrenom()))
                  {
                       alreadyExist=true;
                  }
              }
          }
          if (alreadyExist)
          {
              JOptionPane.showMessageDialog(this, "Le médecin "+ newUser.getNom()+" "+newUser.getPrenom()+" existe déjà.","Médecin existant",JOptionPane.ERROR_MESSAGE);
          }
          else
          {
              _data.add(newUser);
              this.setVisible(false);
          }
       }
    }//GEN-LAST:event_jbtOKActionPerformed

