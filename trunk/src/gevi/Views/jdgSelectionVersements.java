        for (int i = 0; i < indices.length; i++) {
            Versement vers = (Versement) _versements.get(indices[i]);
            if (!_rapport) {
                MainWindow.getMainWindow().displayWindow(MainWindow.CST_VERSEMENT, vers);
            } else {
                MainWindow.getMainWindow().displayWindow(MainWindow.CST_RAPPORT_VERSEMENT, vers);
            }
        }
