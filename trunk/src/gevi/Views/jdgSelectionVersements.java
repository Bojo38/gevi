    public jdgSelectionVersements(java.awt.Frame parent, boolean modal, Document data, Date debut, Date fin, int index, boolean rapport) {
        super(parent, modal);
        initComponents();

        GraphicsEnvironment ge = GraphicsEnvironment.getLocalGraphicsEnvironment();
        GraphicsDevice gs = ge.getDefaultScreenDevice();
        DisplayMode dmode = gs.getDisplayMode();

        if (dmode != null) {
            int screenWidth = dmode.getWidth();
            int screenHeight = dmode.getHeight();
            this.setLocation((screenWidth - this.getWidth()) / 2, (screenHeight - this.getHeight()) / 2);
        }

        this.setIconImage(new javax.swing.ImageIcon(getClass().getResource("/gevi/images/sos_violet.png")).getImage());
        _data = data;
        _rapport = rapport;

        //String[] liste=new String[_data.getCreneaux().size()];
        DefaultListModel model = new DefaultListModel();
        for (int i = 0; i < _data.getVersements().size(); i++) {
            Versement vers = _data.getVersements().get(i);
            Date date = vers.getDate();
            if (date.before(fin) && date.after(debut)) {
                if ((index == 0)||(index==1)) {
                    _versements.add(vers);
                    model.addElement(vers.getName());
                } else {
                    if (vers.getExecutant().equals(_data.getRemplacants().get(index - 2))) {
                        _versements.add(vers);
                        model.addElement(vers.getName());
                    }
                }
            }            
        }
        jlsVersements.setModel(model);
    }

